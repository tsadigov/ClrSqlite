            /// See also: sqlite3FindCollSeq(), sqlite3GetCollSeq()
            ///
            ///</summary>
            public static CollSeq sqlite3LocateCollSeq(Parse pParse, string zName)
            {
                sqlite3 db = pParse.db;
                SqliteEncoding enc = db.aDb[0].pSchema.enc;
