                //    sizeof( char* ) * nCol +        /* Index.azColl     */
                //    u8.Length * nCol +            /* Index.aSortOrder */
                //    nName + 1 +                  /* Index.zName      */
                //    nExtra                       /* Collation sequence names */
                //);
                //if ( db.mallocFailed != 0 )
