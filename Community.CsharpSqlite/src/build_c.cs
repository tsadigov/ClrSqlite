                if (p != null && p.a != null)
                {
                    int i;
                    for (i = p.nSrc - 1; i > 0; i--)
                    {
                        p.a[i].jointype = p.a[i - 1].jointype;
                    }
                    p.a[0].jointype = 0;
                }
            }

            
