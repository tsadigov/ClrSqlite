                    {
                        Debug.Assert(pTable.aCol == null);
                        pTable.nCol = pSelTab.nCol;
                        pTable.aCol = pSelTab.aCol;
                        pSelTab.nCol = 0;
                        pSelTab.aCol = null;
                        TableBuilder.sqlite3DeleteTable(db, ref pSelTab);
                        Debug.Assert(Sqlite3.sqlite3SchemaMutexHeld(db, 0, pTable.pSchema));
                        pTable.pSchema.flags |= sqliteinth.DB_UnresetViews;
                    }
                    else
                    {
                        pTable.nCol = 0;
                        nErr++;
                    }
                    SelectMethods.sqlite3SelectDelete(db, ref pSel);
                }
                else
                {
                    nErr++;
                }
#endif
                return nErr;
            }
